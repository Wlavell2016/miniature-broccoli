◊div[#:class "subhead"]{Et helhetlig anlegg}

◊p{Rødbyen ble bygget som et helhetlig anlegg på totalt åtte bygninger, delt i to av Birkelands gate og en høydeforskjell i terrenget. De to boligene langs Sam Eydes gate hadde felles adkomst fra hovedveien, mens den lavereliggende gruppen hadde forhager mot veien og et indre gårdsrom som åpnet seg mot Mogata og Birkelandsgate. Mellom husrekkene lå det små lave uthus på linje og delte opp gårdsrommet i private soner.
}
