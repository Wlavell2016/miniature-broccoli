◊p{Tunnelen fra Vemork kraftstasjon har et tverrsnitt på 32 m&#178; og er 5660 meter lang med et gjennomsnittlig fall av 1:708 fram til fordelingsbassenget. Sju store steintipper ligger i fjellsiden langs hovedtunnelens løp og danner markante og lesbare
landskapselementer. En egen omløpstunnel med inntakshus og lukearrangement i Måna ved toppen av Kvernhusfossen like ovenfor Rjukanfossen kan føre vannet forbi Vemork direkte til Såheimtoppen. Denne tunnelen tillater drift av Såheim i tilfelle av at Vemork
skulle falle ut av drift eller leilighetsvis stenges. Omløpstunnelen med inntak ble bygd i 1918.}
